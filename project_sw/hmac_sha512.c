/* ===================== RFC4231 summary (counts only) ===================== */
static void run_rfc4231_case(const uint8_t* key, size_t key_len,
    const uint8_t* msg, size_t msg_len,
    const uint8_t* exp, size_t exp_len,
    int tc_index,
    int* p_passed, int* p_total, int* p_failed_case)
{
    uint8_t mac[SHA512_DIGEST_SIZE];
    (*p_total)++;
    hmac_sha512(key, key_len, msg, msg_len, mac);
    if (memcmp(mac, exp, exp_len) == 0) {
        (*p_passed)++;
    }
    else if (p_failed_case && *p_failed_case < 0) {
        *p_failed_case = tc_index;
    }
}

